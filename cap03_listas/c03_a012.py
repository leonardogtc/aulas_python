# ==========================================
# Descobrindo o Tamanho de uma Lista com len()
# Este código demonstra o uso da função len(),
# que calcula e retorna a quantidade de itens na lista.
# ==========================================

# Criando a lista com 4 elementos
carros = ['fiat', 'ferrari', 'lamborghini', 'porsche']

# A função len() (abreviação de 'length', que significa comprimento/tamanho)
# conta quantos itens a lista possui e imprime esse número.
# Para a lista 'carros', ela exibirá: 4
print(len(carros))

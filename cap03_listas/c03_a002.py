# ----- Exibição de valores de uma lista -----
# Criando uma lista de modelos de bicicletas
bicicletas = ['caloi', 'monark', 'trak', 'redline', 'specialized', 'bmx']

# Acessando elementos de uma lista
print(bicicletas)

# Acessando a posição [1]
print(bicicletas[1])

# Acessando a PRIMEIRA posição [0]
print(bicicletas[0])

# Acessando a ÚLTIMA posição [-1]
print(bicicletas[-1])

# Acessando a PENÚLTIMA posição [-2]
print(bicicletas[-2])

# Modificando String de saída
print(bicicletas[1].title())
print(bicicletas[-1].upper())

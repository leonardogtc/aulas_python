# Criação de Listas


# Lista vazia
motocicletas = []
print(type(motocicletas))
print(motocicletas)

# Lista com conteúdo
bicicletas = ['caloi', 'monark', 'bmx']
print(type(bicicletas))
print(bicicletas)

telefones = list()
print(type(telefones))
print(telefones)

# Tupla
numeros = 10, 20, 30, 40, 50
print(type(numeros))
print(numeros)

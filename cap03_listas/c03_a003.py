# ----- Alteração de valores de uma lista -----
# Criando e modificando a lista motocicletas
motocicletas = ['honda', 'yamaha', 'suzuki']
print(motocicletas)

# Modificando Honda por Ducati
motocicletas[0] = 'ducati'
print(motocicletas)

# Modificando suzuki por kawazaki
motocicletas[2] = 'kawazaki'
print(motocicletas)

'''
Dicionários são estruturas dinâmicas, e você pode adicionar novos pares
chave-valor a um dicionário a qualquer momento.
'''

# Adicionando novos pares chave-valor
alien_0 = {
    'cor': 'verde',
    'pontos': 5
}

print(alien_0)

# Acrescentando chave:valor
alien_0['posicao_x'] = 0
alien_0['posicao_y'] = 25

print(alien_0)
